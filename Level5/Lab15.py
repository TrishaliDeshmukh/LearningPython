#Escape Sequence
print("\nEscaping Backslashes: ")
print("This is a backslash \\")
print("She said: \"Hello, world!\"")
print("This is a new line \nThis is the second line")
print("This is a tab\tThis is after the tab")

#if you don't want escape sequences to be interpreted,
# you can use raw strings by prefixing the string literal with an r or R.
print(r"This is a backslash \\")
