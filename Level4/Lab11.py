#String Slicing
# Slicing is the process of obtaining a portion (substring) of a string by using its indices
#string[start:end]

print("Start:end")
my_string = "I am learning Python"
print(my_string[0:4])
print(my_string[1:7])
print(my_string[0:len(my_string)])

print("\n sart:end:step")
#string[start:end:step]
print(my_string[0:4:2])
print(my_string[1:7:2])
print(my_string[0:len(my_string):2])